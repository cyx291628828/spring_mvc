package com.source.plan.beans;

/*
 * 用户类
 */
public class User {
	String userID;  //用户ID
	String username; //用户名字
	String userRegTime; //用户注册时间
	String userSex;	//用户性别
	String userBirth; //用户出生日期
	String userJob; //用户职业
	String userFakeMoney; //用户虚拟币数量
	String userInterest; //用户名言
	String userLevel; //用户等级
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserRegTime() {
		return userRegTime;
	}
	public void setUserRegTime(String userRegTime) {
		this.userRegTime = userRegTime;
	}
	public String getUserSex() {
		return userSex;
	}
	public void setUserSex(String userSex) {
		this.userSex = userSex;
	}
	public String getUserBirth() {
		return userBirth;
	}
	public void setUserBirth(String userBirth) {
		this.userBirth = userBirth;
	}
	public String getUserJob() {
		return userJob;
	}
	public void setUserJob(String userJob) {
		this.userJob = userJob;
	}
	public String getUserFakeMoney() {
		return userFakeMoney;
	}
	public void setUserFakeMoney(String userFakeMoney) {
		this.userFakeMoney = userFakeMoney;
	}
	public String getUserInterest() {
		return userInterest;
	}
	public void setUserInterest(String userInterest) {
		this.userInterest = userInterest;
	}
	public String getUserLevel() {
		return userLevel;
	}
	public void setUserLevel(String userLevel) {
		this.userLevel = userLevel;
	}
	public String getUserImage() {
		return userImage;
	}
	public void setUserImage(String userImage) {
		this.userImage = userImage;
	}
	String userImage;
}
